    private void btnResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolverActionPerformed

        //Verificar campos
        if (x1.getText().trim().isEmpty() || x2.getText().trim().isEmpty() || x3.getText().trim().isEmpty()
                || y1.getText().trim().isEmpty() || y2.getText().trim().isEmpty() || y3.getText().trim().isEmpty()
                || z1.getText().trim().isEmpty() || z2.getText().trim().isEmpty() || z3.getText().trim().isEmpty()
                || ti1.getText().trim().isEmpty() || ti2.getText().trim().isEmpty() || ti3.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Debe ingresar todos los valores respectivos.");
            return;
        }

        try {
            //Convertir texto a numeros decimales. 
            float X1 = Float.parseFloat(x1.getText());
            float X2 = Float.parseFloat(x2.getText());
            float X3 = Float.parseFloat(x3.getText());

            float Y1 = Float.parseFloat(y1.getText());
            float Y2 = Float.parseFloat(y2.getText());
            float Y3 = Float.parseFloat(y3.getText());

            float Z1 = Float.parseFloat(z1.getText());
            float Z2 = Float.parseFloat(z2.getText());
            float Z3 = Float.parseFloat(z3.getText());

            float TI1 = Float.parseFloat(ti1.getText());
            float TI2 = Float.parseFloat(ti2.getText());
            float TI3 = Float.parseFloat(ti3.getText());

            // CÃ¡lculo del determinante principal
            float detA = (X1 * Y2 * Z3) + (Y1 * Z2 * X3) + (Z1 * X2 * Y3)
                    - (Y1 * X2 * Z3) - (X1 * Z2 * Y3) - (Z1 * Y2 * X3);

            if (detA == 0) {
                txtRespuestaX.setText("Sin soluciÃ³n");
                txtResultadoY.setText("Sin soluciÃ³n");
                txtResultadoZ.setText("Sin soluciÃ³n");
                return;
            }

            //TI = Termino independiente
            float detAx = (TI1 * Y2 * Z3) + (Y1 * Z2 * TI3) + (Z1 * TI2 * Y3)
                    - (Y1 * TI2 * Z3) - (TI1 * Z2 * Y3) - (Z1 * Y2 * TI3);

            float detAy = (X1 * TI2 * Z3) + (TI1 * Z2 * X3) + (Z1 * X2 * TI3)
                    - (TI1 * X2 * Z3) - (X1 * Z2 * TI3) - (Z1 * TI2 * X3);

            float detAz = (X1 * Y2 * TI3) + (Y1 * TI2 * X3) + (TI1 * X2 * Y3)
                    - (Y1 * X2 * TI3) - (X1 * TI2 * Y3) - (TI1 * Y2 * X3);

            float x = detAx / detA;
            float y = detAy / detA;
            float z = detAz / detA;

            txtRespuestaX.setText(String.format("%.2f", x));
            txtResultadoY.setText(String.format("%.2f", y));
            txtResultadoZ.setText(String.format("%.2f", z));

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar todos los valores respectivos.");
        }
    }//GEN-LAST:event_btnResolverActionPerformed

