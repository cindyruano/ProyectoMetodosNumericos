    private void btnResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolverActionPerformed

        float X1 = Float.parseFloat(x1.getText()); //hace la cadena en un valor flotante
        float X2 = Float.parseFloat(x2.getText());
        float X3 = Float.parseFloat(x3.getText());

        float Y1 = Float.parseFloat(y1.getText());
        float Y2 = Float.parseFloat(y2.getText());
        float Y3 = Float.parseFloat(y3.getText());

        float Z1 = Float.parseFloat(z1.getText());
        float Z2 = Float.parseFloat(z2.getText());
        float Z3 = Float.parseFloat(z3.getText());

        float TI1 = Float.parseFloat(ti1.getText());
        float TI2 = Float.parseFloat(ti2.getText());
        float TI3 = Float.parseFloat(ti3.getText());

        //termino independiente = detA
        float detA = (((X1 * Y2 * Z3) + (Y1 * Z2 * X3) + (Z1 * X2 * Y3)) - ((Y1 * X2 * Z3) + (X1 * Z2 * Y3) + (Z1 * Y2 * X3)));

        float detAx = (((TI1 * Y2 * Z3) + (Y1 * Z2 * TI3) + (Z1 * TI2 * Y3)) - ((Y1 * TI2 * Z3) + (TI1 * Z2 * Y3) + (Z1 * Y2 * TI3)));
        float detAy = (((X1 * TI2 * Z3) + (TI1 * Z2 * X3) + (Z1 * X2 * TI3)) - ((TI1 * X2 * Z3) + (X1 * Z2 * TI3) + (Z1 * TI2 * X3)));
        float detAz = (((X1 * Y2 * TI3) + (Y1 * TI2 * X3) + (TI1 * X2 * Y3)) - ((Y1 * X2 * TI3) + (X1 * TI2 * Y3) + (TI1 * Y2 * X3)));

        float x = detAx / detA;
        float y = detAy / detA;
        float z = detAz / detA;

        String respuesta = "X: " + x + "\nY: " + y + "\nZ: " + z;
        areA.setText(respuesta);
    }//GEN-LAST:event_btnResolverActionPerformed

